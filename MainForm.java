    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        
        if(jdc_dateRecorded.getDate()==null){
            JOptionPane.showMessageDialog(null, "Please choose a date.");
            jdc_dateRecorded.requestFocus();
            return;
        }if(getTxt_idnumber().getText().length()==0){
            JOptionPane.showMessageDialog(null, "ID Number is required field. ");
            txt_idnumber.requestFocus();
            return;
        }else if(getTxt_fname().getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Firstname is required field. ");
            txt_fname.requestFocus();
            return;
        }else if(getTxt_lname().getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Lastname is required field. ");
            txt_lname.requestFocus();
            return;
        }else if(getTxt_designation().getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Designation is required field. ");
            txt_designation.requestFocus();
            return;
        }else if(getTxt_locale().getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Locale is required field. ");
            txt_locale.requestFocus();
            return;
        }else if(getTxt_district().getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "District is required field. ");
            txt_district.requestFocus();  
            return;        
        }else if(getFtxt_tulong().getValue() == null){
            ftxt_tulong.setValue(0.0);
        }else if(getTxt_remarks().getText().isEmpty()){
            txt_remarks.setText(" ");
        }
        
        saveNewRecord();
       
        fillTblWorkerInfo();
        txt_idnumber.setText("");        
        txt_fname.setText("");
        txt_mname.setText("");
        txt_lname.setText("");
        txt_locale.setText("");
        txt_district.setText("");
        txt_designation.setText("");
        cbo_category.setSelectedIndex(1);
        ftxt_tulong.setValue(null);
        txt_remarks.setText("");
        txt_idnumber.requestFocus();
        cid = 0;
        wid = 0;
    }//GEN-LAST:event_btn_saveActionPerformed

