    private void fillValuesToFieldFromTable(){
        try{
        int row = tbl_workerInfo.getSelectedRow();
        String id = tbl_workerInfo.getModel().getValueAt(row, 0).toString();
        DataAccessForm daf = new DataAccessForm();
        Object data[];
        data = daf.workerData(id);
        txt_idnumber.setText(id);        
        txt_fname.setText(data[1].toString());
        txt_mname.setText(data[2].toString());
        txt_lname.setText(data[3].toString());
        txt_locale.setText(data[4].toString());
        txt_district.setText(data[5].toString());
        txt_designation.setText(tbl_workerInfo.getModel().getValueAt(row,2).toString());
        cbo_category.setSelectedItem(tbl_workerInfo.getModel().getValueAt(row, 3).toString());
        ftxt_tulong.setValue(tbl_workerInfo.getModel().getValueAt(row, 5));
        txt_remarks.setText(tbl_workerInfo.getModel().getValueAt(row, 7).toString());
        ((JTextField)jdc_dateRecorded.getDateEditor().getUiComponent()).
                setText(tbl_workerInfo.getModel().getValueAt(row, 4).toString());
        wid = Integer.parseInt(data[0].toString());
        cid = Integer.parseInt(tbl_workerInfo.getModel().getValueAt(row, 8).toString());
        btn_save.setEnabled(false);
        if(btn_edit.isEnabled())
            setFieldsNotEditable();
        }catch(Exception e){
            
        }
    }
